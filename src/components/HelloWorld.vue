<template>
  <div class="hello">
    <input v-model.number="operand1"/>
    <input v-model.number="operand2"/>
    = {{ result }}
    <div>
      <button @click="calculate('+')">+</button>
      <button @click="calculate('-')">-</button>
      <button @click="calculate('/')">/</button>
      <button @click="calculate('*')">*</button>
      <button @click="calculate('**')">**</button>
      <button @click="calculate('%')">%</button>
    </div>
  </div>
</template>

<script>
export default {
  
  data: () => ({
 operand1: 0,
 operand2: 0,
 result: 0
  }),
  props: {
    
  },
  methods: {   
    
  calculate (operation) {
    switch(operation) {
    case '+' :
      this.result = this.operand1 + this.operand2;
      break;
    case '-' :
      this.result = this.operand1 - this.operand2;
      break;  
    case '/' :
      this.result = this.operand1 / this.operand2;
      break; 
    case '*' :
      this.result = this.operand1 * this.operand2;
      break; 
      case '**' :
      this.result = this.operand1 ** this.operand2;
      break; 
      case '%' :
      this.result = this.operand1 % this.operand2;
      break; 
      }
  }
    /*
